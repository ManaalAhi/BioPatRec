% --- Executes just before GUI_SendKeys is made visible.
function GUI_SendKeys_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to GUI_SendKeys (see VARARGIN)

% Choose default command line output for GUI_SendKeys
handles.output = hObject;

% Update handles structure
guidata(hObject, handles);

if ~isempty(varargin)
 inObj = varargin{1};
 
 mainHandles = guidata(inObj);
 
 movements = mainHandles.patRec.mov;
 handles.mov = movements;
 
 numberOfMovements = mainHandles.patRec.nOuts;
 
 for i = 1:numberOfMovements
     obj = sprintf('txt_%i',i);
     set(handles.(obj),'String',movements{i});     
 end
 for i = numberOfMovements:7
     obj = sprintf('txt_%i',i);
     set(handles.(obj),'Visible','off');
     obj = sprintf('popupmenu%i',i);
     set(handles.(obj),'Visible','off');
 end
 for i = 1:7
    obj = sprintf('popupmenu%i',i);
    set(handles.(obj),'String',{'-- Select Key --','W','A','S','D'});
    if isfield(mainHandles,'savedKeys') %If the keys have already been loaded; load the previous values into popupmenus.
        set(handles.(obj),'Value',mainHandles.savedKeys(i) + 1);
    end
 end
end

handles.main = inObj;

guidata(hObject,handles);

